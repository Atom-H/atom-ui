<template>
    <!-- radio改为圆形, 为了区别checkbox-->
    <div :class="['component-radio', disabled && 'component-radio-disabled']">
        <label>
            <span v-if="!!$slots.default" class="title">
                <slot></slot>
            </span>
            <span class="radio" :class="{checked: isChecked}">
                <input :disabled="disabled" :value="value" :checked="isChecked" @change="change" @click.stop="()=>{}" type="radio">
                <span class="appearance"></span>
            </span>
        </label>
    </div>
</template>
<script>
export default {
    name: 'Radio',

    model: {
        prop: 'referenceValue',
        event: 'change'
    },

    props: {
        disabled: {
            type: Boolean,
            default: false
        },
        // 外部参考值
        // checked: referenceValue == value
        referenceValue: {
            required: true
        },

        value: {
            required: true
        }
    },
    methods: {
        change() {
            if (!this.disabled) {
                this.$emit('change', this.value);
            }
        }
    },

    computed: {
        isChecked() {
            return this.value == this.referenceValue;
        }
    }
}
</script>
